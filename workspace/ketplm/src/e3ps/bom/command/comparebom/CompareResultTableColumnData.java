package e3ps.bom.command.comparebom;

/**
 * class Name  : CompareResultTableColumnData
 * Description : 비교결과를 보여주는 Table의 ColumnData
 */
public class CompareResultTableColumnData
{
	private String  strColumnName;
	private int     width;
	private int     alignment;

	public CompareResultTableColumnData(String strColumnName, int width, int alignment)
	{
		this.strColumnName = strColumnName;
		this.width = width;
		this.alignment = alignment;
	}

	/**
	 * ColumnName을 반환한다.
	 * @return String ColumnName
	 */
	public String getColumnName()
	{
		return strColumnName;
	}

	/**
	 * ColumnWidth를 반환한다.
	 * @return int Width
	 */
	public int getWidth()
	{
		return width;
	}

	/**
	 * ColumnAlignment를 반환한다.
	 * @return int Alignment
	 */
	public int getAlignment()
	{
		return alignment;
	}

}

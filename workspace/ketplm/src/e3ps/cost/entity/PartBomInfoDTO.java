package e3ps.cost.entity;

import org.apache.commons.lang.builder.ToStringBuilder;
import org.apache.commons.lang.builder.ToStringStyle;

final public class PartBomInfoDTO {


    private String ChildItemCode;
    
    private String GroupNo;
    
    private String LVL;
    
    private String case_count_1;
    
    private String case_count_2;
    
    private String qty;

    public PartBomInfoDTO() {
    }


    public String getChildItemCode() {
        return ChildItemCode;
    }


    public void setChildItemCode(String childItemCode) {
        ChildItemCode = childItemCode;
    }


    public String getGroupNo() {
        return GroupNo;
    }


    public void setGroupNo(String groupNo) {
        GroupNo = groupNo;
    }
    

    public String getLVL() {
        return LVL;
    }


    public void setLVL(String lVL) {
        LVL = lVL;
    }


    public String getCase_count_1() {
        return case_count_1;
    }


    public void setCase_count_1(String case_count_1) {
        this.case_count_1 = case_count_1;
    }


    public String getCase_count_2() {
        return case_count_2;
    }


    public void setCase_count_2(String case_count_2) {
        this.case_count_2 = case_count_2;
    }
   

    public String getQty() {
        return qty;
    }


    public void setQty(String qty) {
        this.qty = qty;
    }


    public String toString() {
	return ToStringBuilder.reflectionToString(this, ToStringStyle.MULTI_LINE_STYLE);
    }
}

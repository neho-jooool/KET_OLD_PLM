package e3ps.edm.clients.batch;

public class EPMTableColumnData {

	public String	m_title;
	public int		 m_width;
	public int		 m_alignment;
	
	public boolean m_isHideshow = false;

	public EPMTableColumnData(String title, int width, int alignment, boolean isHideshow) {
		m_title = title;
		m_width = width;
		m_alignment = alignment;
		m_isHideshow = isHideshow;
	}
}

package ext.ket.part.migration.erp;

import java.io.Serializable;

import org.apache.commons.lang.builder.ToStringBuilder;
import org.apache.commons.lang.builder.ToStringStyle;

public class ErpMoldPartDTO implements Serializable {

    private String partNo;
    private String partName;
    private String partUnit;
    private String partType;
    private String spMaterDie;
    private String spIsDeleted;
    private String excelFileName;

    public String getPartNo() {
	return partNo;
    }

    public void setPartNo(String partNo) {
	this.partNo = partNo;
    }

    public String getPartName() {
	return partName;
    }

    public void setPartName(String partName) {
	this.partName = partName;
    }

    public String getPartUnit() {
	return partUnit;
    }

    public void setPartUnit(String partUnit) {
	this.partUnit = partUnit;
    }

    public String getPartType() {
	return partType;
    }

    public void setPartType(String partType) {
	this.partType = partType;
    }

    public String getSpMaterDie() {
	return spMaterDie;
    }

    public void setSpMaterDie(String spMaterDie) {
	this.spMaterDie = spMaterDie;
    }

    public String getSpIsDeleted() {
	return spIsDeleted;
    }

    public void setSpIsDeleted(String spIsDeleted) {
	this.spIsDeleted = spIsDeleted;
    }

    public String getExcelFileName() {
        return excelFileName;
    }

    public void setExcelFileName(String excelFileName) {
        this.excelFileName = excelFileName;
    }

    public String toString() {
	return ToStringBuilder.reflectionToString(this, ToStringStyle.MULTI_LINE_STYLE);
    }

}

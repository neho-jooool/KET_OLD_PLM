package ext.ket.part.migration.revision;

import org.apache.commons.lang.builder.ToStringBuilder;
import org.apache.commons.lang.builder.ToStringStyle;

public class PartRevisionDTO {

    private long mastId;
    private long revId;
    private long iterId;

    private String mastOid;
    private String revOid;
    private String iterOid;

    private String epmNo;
    private String epmName;
    private String verNo;
    private String verSortNo;
    private String iterNo;
    
    private String stateCheckState;

    public long getMastId() {
	return mastId;
    }

    public void setMastId(long mastId) {
	this.mastId = mastId;
    }

    public long getRevId() {
	return revId;
    }

    public void setRevId(long revId) {
	this.revId = revId;
    }

    public long getIterId() {
	return iterId;
    }

    public void setIterId(long iterId) {
	this.iterId = iterId;
    }

    public String getMastOid() {
	return mastOid;
    }

    public void setMastOid(String mastOid) {
	this.mastOid = mastOid;
    }

    public String getRevOid() {
	return revOid;
    }

    public void setRevOid(String revOid) {
	this.revOid = revOid;
    }

    public String getIterOid() {
	return iterOid;
    }

    public void setIterOid(String iterOid) {
	this.iterOid = iterOid;
    }

    public String getEpmNo() {
	return epmNo;
    }

    public void setEpmNo(String epmNo) {
	this.epmNo = epmNo;
    }

    public String getEpmName() {
	return epmName;
    }

    public void setEpmName(String epmName) {
	this.epmName = epmName;
    }

    public String getVerNo() {
	return verNo;
    }

    public void setVerNo(String verNo) {
	this.verNo = verNo;
    }

    public String getVerSortNo() {
	return verSortNo;
    }

    public void setVerSortNo(String verSortNo) {
	this.verSortNo = verSortNo;
    }

    public String getIterNo() {
	return iterNo;
    }

    public void setIterNo(String iterNo) {
	this.iterNo = iterNo;
    }

    public String getStateCheckState() {
        return stateCheckState;
    }

    public void setStateCheckState(String stateCheckState) {
        this.stateCheckState = stateCheckState;
    }

    public String toString() {
	return ToStringBuilder.reflectionToString(this, ToStringStyle.MULTI_LINE_STYLE);
    }

}

package ext.ket.part.entity.dto;

import java.io.Serializable;

import org.apache.commons.lang.builder.ToStringBuilder;
import org.apache.commons.lang.builder.ToStringStyle;

public class PartValidationDTO implements Serializable {

    private String validType;
    private String validPartOid;
    private String validPartNumber;
    private String validContent;
    
    public String getValidType() {
        return validType;
    }
    public void setValidType(String validType) {
        this.validType = validType;
    }
    public String getValidPartOid() {
        return validPartOid;
    }
    public void setValidPartOid(String validPartOid) {
        this.validPartOid = validPartOid;
    }
    public String getValidPartNumber() {
        return validPartNumber;
    }
    public void setValidPartNumber(String validPartNumber) {
        this.validPartNumber = validPartNumber;
    }
    public String getValidContent() {
        return validContent;
    }
    public void setValidContent(String validContent) {
        this.validContent = validContent;
    }
    public String toString() {
	return ToStringBuilder.reflectionToString(this, ToStringStyle.MULTI_LINE_STYLE);
    }
    
}

package ext.ket.cost.util;

import java.io.Serializable;

/*********************************************************
 * @description
 * @author khkim
 * @date 2018. 2. 15. 오후 2:15:40
 * @Pakage ext.ket.cost.util
 * @class BomEditorColumnInfo
 *********************************************************/
@SuppressWarnings("serial")
public class BomEditorColumnInfo implements Serializable {

    private String columKey;
    private String displayName;
    private String description;

    private boolean view;
    private boolean editor;

    private boolean necessary;
    private boolean disabled;

    public BomEditorColumnInfo(String columKey, String displayName, String description) {
        this.columKey = columKey;
        this.displayName = displayName;
        this.description = description;
    }

    /**
     * @return the columnKey
     */
    public String getColumKey() {
        return columKey;
    }

    /**
     * @param columnKey
     *            the columnKey to set
     */
    public void setColumKey(String columKey) {
        this.columKey = columKey;
    }

    /**
     * @return the displayName
     */
    public String getDisplayName() {
        return displayName;
    }

    /**
     * @param displayName
     *            the displayName to set
     */
    public void setDisplayName(String displayName) {
        this.displayName = displayName;
    }

    /**
     * @return the description
     */
    public String getDescription() {
        return description;
    }

    /**
     * @param description
     *            the description to set
     */
    public void setDescription(String description) {
        this.description = description;
    }

    /**
     * @return the view
     */
    public boolean isView() {
        return view;
    }

    /**
     * @param view
     *            the view to set
     */
    public void setView(boolean view) {
        this.view = view;
    }

    /**
     * @return the editor
     */
    public boolean isEditor() {
        return editor;
    }

    /**
     * @param editor
     *            the editor to set
     */
    public void setEditor(boolean editor) {
        this.editor = editor;
    }

    public boolean isNecessary() {
        return necessary;
    }

    public void setNecessary(boolean necessary) {
        this.necessary = necessary;
    }

    public boolean isDisabled() {
        return disabled;
    }

    public void setDisabled(boolean disabled) {
        this.disabled = disabled;
    }

}

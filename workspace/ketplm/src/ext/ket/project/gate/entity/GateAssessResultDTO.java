import ext.ket.shared.dto.BaseDTO;

public class GateAssessResultDTO extends BaseDTO {
    private String taskOid = "";
    private String projectOid = "";
    private String finalResult = "";
    private String isFileDel = "";
    private String passComment = "";

    public String getTaskOid() {
	return taskOid;
    }

    public void setTaskOid(String taskOid) {
	this.taskOid = taskOid;
    }

    public String getProjectOid() {
	return projectOid;
    }

    public void setProjectOid(String projectOid) {
	this.projectOid = projectOid;
    }

    public String getFinalResult() {
	return finalResult;
    }

    public void setFinalResult(String finalResult) {
	this.finalResult = finalResult;
    }

    public String getIsFileDel() {
	return isFileDel;
    }

    public void setIsFileDel(String isFileDel) {
	this.isFileDel = isFileDel;
    }

    public String getPassComment() {
	return passComment;
    }

    public void setPassComment(String passComment) {
	this.passComment = passComment;
    }

}

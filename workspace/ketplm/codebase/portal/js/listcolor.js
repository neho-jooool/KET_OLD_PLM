var selectObj;
function changeColor(tr_id){
	if(tr_id!=null){
		var obj = document.getElementById(tr_id);
		if(selectObj!=null){
			selectObj.style.backgroundColor='#ffffff'
		}
		selectObj = obj;
		selectObj.style.backgroundColor='#ECF4D4';
	}
}
function onOver(obj){
	if(obj != selectObj){
		obj.style.backgroundColor='#f5f5f5';
	}
	//obj.style.cursor='hand';
}
function onOut(obj){
	if(obj != selectObj){
		obj.style.backgroundColor='#ffffff'
	}
}
